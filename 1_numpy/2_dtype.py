import numpy as np

float_array = np.array([1, 2, 3, 4, 5, 6], dtype=float)
print(f"Float Array: {float_array}")
print(f"Data Type: {float_array.dtype}")

complex_array = np.array([1, 2, 3, 4, 5, 6], dtype=np.complex128)
print(f"\nComplex Array: {complex_array}")
print(f"Data Type: {complex_array.dtype}")
